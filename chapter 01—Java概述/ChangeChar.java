public class ChangeChar {
    public static void main(String[] args) {
        // \t ：一个制表位，实现对齐的功能
        System.out.println("北京\t天津\t上海");

        // \n ：换行符
        System.out.println("hsdsdsdvs\nsdjnscsd\nsdvsds\n");
    }
}
